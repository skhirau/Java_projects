    private void cb3ItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cb3ItemStateChanged
        if(flag == false)
            return;
        if(cb3.getSelectedIndex() == 0)
        {
            DefaultComboBoxModel dcbm = (DefaultComboBoxModel) cb4.getModel();
            dcbm.removeAllElements();
            return;
        }
        
        Connection con = null;
        Statement st = null;
        ResultSet rs = null;
        String sql;
        try
        {
            try
            {
                Class.forName("java.sql.Driver");
                con = DriverManager.getConnection("jdbc:mysql://localhost:3306/store","root","admin");
                st = con.createStatement();
                sql = "select * from compartments where compname = '" + 
                        cb2.getSelectedItem() + "';";
                rs = st.executeQuery(sql);
                rs.next();
                compno = rs.getInt("compno");
                rs.close();
                
                sql = "select * from shelftype where typename = '" +
                        cb3.getSelectedItem() + "';";
                rs = st.executeQuery(sql);
                rs.next();
                typeno = rs.getInt("typeno");
                rs.close();
                
                DefaultComboBoxModel dcbm = (DefaultComboBoxModel) cb4.getModel();
                dcbm.removeAllElements();
                dcbm.addElement("--Select--");
                
                sql = "select * from shelf where compno = " + compno + " and typeno = " + 
                        typeno + ";";
                rs = st.executeQuery(sql);
                if(rs.next())
                {    
                    dcbm.addElement("" + rs.getString("shelfno"));
                    while(rs.next())
                    {
                        dcbm.addElement("" + rs.getString("shelfno"));
                    }
                    
                }
                else
                    JOptionPane.showMessageDialog(this, "No shelf are present in this category.");
                rs.close();
            }
            catch(Exception e)
            {
                JOptionPane.showMessageDialog(this,e.getMessage());
            }
            finally
            {
                if(!rs.isClosed())
                rs.close();
                if(!st.isClosed())
                st.close();
                if(!con.isClosed())
                con.close();
            }
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(this,"Badi Gadbad.");
        }
    }//GEN-LAST:event_cb3ItemStateChanged

