    private void jComboBox2ItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_jComboBox2ItemStateChanged
        if(flag == false)
            return;
              
        if(jComboBox2.getSelectedIndex() == 0)
        {
            jTextField1.setText("");
            return;
        }
        if(jComboBox1.getSelectedIndex() == 0)
        {
            JOptionPane.showMessageDialog(this,"Please first select a valid Compartment first.");
            return;
        }
        Connection con = null;
        Statement st = null;
        ResultSet rs = null;
        String sql;
        try
        {
            try
            {
                Class.forName("java.sql.Driver");
                con = DriverManager.getConnection("jdbc:mysql://localhost:3306/store","root","admin");
                st = con.createStatement();
                sql = "select * from compartments where compname = '" + 
                        jComboBox1.getSelectedItem() + "';";
                rs = st.executeQuery(sql);
                rs.next();
                compno = rs.getInt("compno");
                rs.close();
                
                sql = "select * from shelftype where typename = '" +
                        jComboBox2.getSelectedItem() + "';";
                rs = st.executeQuery(sql);
                rs.next();
                typeno = rs.getInt("typeno");
                rs.close();

                sql = "select * from shelf where compno = " + compno + " and typeno = " + 
                        typeno + ";";
                rs = st.executeQuery(sql);
                if(rs.next())
                {
                    jTextField1.setText("" + (1 + rs.getInt("shelfno")));
                }
                else
                    jTextField1.setText("1");
                
            }
            catch(Exception e)
            {
                JOptionPane.showMessageDialog(this,e.getMessage());
            }
            finally
            {
                if(!rs.isClosed())
                rs.close();
                if(!st.isClosed())
                st.close();
                if(!con.isClosed())
                con.close();
            }
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(this,"Badi Gadbad.");
        }
    }//GEN-LAST:event_jComboBox2ItemStateChanged


