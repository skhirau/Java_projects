    private void addIntoComboBoxes()
    {
        flag = false;
        DefaultComboBoxModel dcbm1 = (DefaultComboBoxModel) jComboBox1.getModel();
        DefaultComboBoxModel dcbm2 = (DefaultComboBoxModel) jComboBox2.getModel();
        Connection con = null;
        Statement st = null;
        ResultSet rs = null;
        String sql;
        try
        {
            try
            {
                Class.forName("java.sql.Driver");
                con = DriverManager.getConnection("jdbc:mysql://localhost:3306/store","root","admin");
                st = con.createStatement();

                sql = "select * from compartments;";
                rs = st.executeQuery(sql);
                dcbm1.removeAllElements();
                dcbm1.addElement("--Select--");
                while(rs.next())
                {
                    dcbm1.addElement("" + rs.getString("compname"));
                }
                rs.close();
                
                sql = "select * from shelftype;";
                rs = st.executeQuery(sql);
                dcbm2.removeAllElements();
                dcbm2.addElement("--Select--");
                while(rs.next())
                {
                    dcbm2.addElement("" + rs.getString("typename"));
                }
                rs.close();
                flag = true;
                jComboBox1.setSelectedIndex(0);
                jComboBox2.setSelectedIndex(0);
            }
            catch(Exception e)
            {
                JOptionPane.showMessageDialog(this,e.getMessage());
            }
            finally
            {
                if(!rs.isClosed())
                rs.close();
                if(!st.isClosed())
                st.close();
                if(!con.isClosed())
                con.close();
            }
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(this,"Badi Gadbad.");
        }
    }

