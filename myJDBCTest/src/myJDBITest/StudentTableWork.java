        Statement st = null;
        ResultSet rs = null;
        String sql = "";
        try
        {
            try
            {
                Class.forName("java.sql.Driver");
                con = DriverManager.getConnection("jdbc:mysql://localhost:3306/studentdb","root","admin");
                st = con.createStatement();
                sql =  "insert into student values (" + jTextField1.getText() + "," +
                        jTextField2.getText() + ",'" + jTextField3.getText() + "','"+
                        jTextField4.getText() + "','" + jTextField5.getText() + "'," +
                        jTextField6.getText() + ");";
                st.executeUpdate(sql);
                jTextField1.setText("");
                jTextField2.setText("");
                jTextField3.setText("");
                jTextField4.setText("");
                jTextField5.setText("");
                jTextField6.setText("12");
                
                sql = "select * from student;";
                rs = st.executeQuery(sql);
                DefaultListModel dlm = (DefaultListModel) jList1.getModel();
                dlm.removeAllElements();
                while(rs.next())
                {
                    dlm.addElement(rs.getString("fname"));
                }
                
            }
            catch (SQLException e) 
            {
                JOptionPane.showMessageDialog(this,"Locha  " + e.getMessage());
            } 
            finally
            {
                rs.close();
                st.close();
                con.close();
            }
            
        } 
        catch (Exception e) 
        {
            JOptionPane.showMessageDialog(this,"Bada Locha " + e.getMessage());
        }
        
        
